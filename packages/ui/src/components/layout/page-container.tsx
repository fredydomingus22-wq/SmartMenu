"use client";

import { ReactNode } from "react";
import { cn } from "@/lib/utils";

interface PageContainerProps {
    children: ReactNode;
    className?: string;
    size?: "sm" | "md" | "lg" | "xl" | "full";
}

export function PageContainer({
    children,
    className = "",
    size = "lg"
}: PageContainerProps) {
    const sizeClasses = {
        sm: "max-w-2xl",
        md: "max-w-4xl",
        lg: "max-w-6xl",
        xl: "max-w-7xl",
        full: "max-w-full"
    };

    return (
        <div className={cn(
            "w-full mx-auto px-4 sm:px-6 lg:px-8",
            sizeClasses[size],
            className
        )}>
            {children}
        </div>
    );
}